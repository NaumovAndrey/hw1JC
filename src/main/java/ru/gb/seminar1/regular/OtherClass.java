package ru.gb.seminar1.regular;

/**
 * Класс с арифмитическими методами
 */
public class OtherClass {
    /**
     * Метод сумирования двух чисел
     * @param a
     * @param b
     * @return
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * Метод деления двух чисел
     * @param a делимое
     * @param b делитель
     * @return
     */
    public static int div(int a, int b) {
        return a / b;
    }

    /**
     * Метод умножения двух чисел
     * @param a
     * @param b
     * @return
     */
    public static int mul(int a, int b) {
        return a * b;
    }

    /**
     * Метод разности двух чисел
     * @param a
     * @param b
     * @return
     */
    public static int sub (int a, int b){
        return a - b;
    }
}
